CREATE TABLE FOOD_CATEGORY (
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   NAME VARCHAR(100),
   PRIMARY KEY (ID)
);

CREATE TABLE FOOD (
   EK CHAR(1),
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   NAME VARCHAR(100),
   ABBREVIATION VARCHAR(100),
   CODE VARCHAR(100),
   DESCRIPTION VARCHAR(3000),
   CATEGORY BIGINT,
   STATUS CHAR(1),
   PRIMARY KEY (ID),
   FOREIGN KEY (CATEGORY) REFERENCES FOOD_CATEGORY(ID)
);

CREATE TABLE SALE_SITE (
   EK CHAR(1),
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   NAME VARCHAR(100),
   DESCRIPTION VARCHAR(3000),
   STATUS CHAR(1),
   PRIMARY KEY (ID)
);

CREATE TABLE SALE_PLACE (
   EK CHAR(1),
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   NAME VARCHAR(100),
   DESCRIPTION VARCHAR(3000),
   TYPE CHAR(1),
   SALE_SITE BIGINT,
   SERVICE_STATUS CHAR(1),
   STATUS CHAR(1),
   PRIMARY KEY (ID),
   FOREIGN KEY (SALE_SITE) REFERENCES SALE_SITE(ID)
);

CREATE TABLE OPERATOR (
   EK CHAR(1),
   ID VARCHAR(50),
   NAME VARCHAR(100),
   DESCRIPTION VARCHAR(3000),
   STATUS CHAR(1),
   PRIMARY KEY (ID)
);

CREATE TABLE OPERATOR_SALE_SITE (
   OPERATOR_ID VARCHAR(50),
   SALE_SITE_ID BIGINT
);

CREATE TABLE BILL (
   EK CHAR(1),
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   BOUGHT_DATE DATE,
   BOUGHT_TIME TIME,
   CHECKOUT_DATE DATE,
   CHECKOUT_TIME TIME,
   SALE_PLACE_ID BIGINT,
   SALE_PLACE_NAME VARCHAR(100),
   SALE_SITE_ID BIGINT,
   SALE_SITE_NAME VARCHAR(100),
   OPERATOR_ID BIGINT,
   OPERATOR_NAME VARCHAR(100),
   PRICE NUMERIC(10,2),
   SELLING_PRICE NUMERIC(10,2),
   COMMENT VARCHAR(3000),
   PRIMARY KEY (ID)
);

CREATE TABLE BILL_ITEM (
   EK CHAR(1),
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   BILL BIGINT,
   FOOD_ID BIGINT,
   FOOD_NAME VARCHAR(1000),
   FOOD_COUNT INTEGER,
   PRICE NUMERIC(10,2),
   OPERATOR_ID BIGINT,
   OPERATOR_NAME VARCHAR(1000),
   HASTEN_COUNT INTEGER,
   COMMENT VARCHAR(3000),
   STATUS CHAR(1),
   PRIMARY KEY (ID),
   FOREIGN KEY (MENU) REFERENCES BILL(ID)
);

CREATE TABLE BILL_ITEM_HASTEN_RECORD (
   BILL BIGINT,
   BILL_ITEM BIGINT
)

CREATE TABLE SPECIAL_EVENT (
   EK CHAR(1),
   ID BIGINT,
   TITLE VARCHAR(1000),
   DETAIL VARCHAR(3000),
   HAPPENED_DATE DATE,
   HAPPENED_TIME TIME,
   COST NUMERIC(10,2),
   TYPE CHAR(1),
   PRIMARY KEY (ID)
);
